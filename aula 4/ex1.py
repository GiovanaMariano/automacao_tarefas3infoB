"""
Cie um programa que recebe como entrada dois números reais. o programa deve imprimir as quatros operações matemáticas entre os dois números
( soma, subtração, divisão e multiplicação)
"""

print('Digite dois números: ')
número1 = int(input())
número2 = int(input())

númeroS = número1 + número2
print("A soma é:", númeroS)
númeroSu = número1 - número2
print("A subtração é:", númeroSu)
númeroD = número1 / número2
